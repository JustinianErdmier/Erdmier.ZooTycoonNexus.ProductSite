namespace Erdmier.ZooTycoonNexus.Presentation.Web.Common.Constants;

/// <summary>Defines the CSS token kinds used to colour values in a feature card's INI snippet preview.</summary>
public enum IniEntryKinds
{
    /// <summary>A key-name-style entry, rendered in amber.</summary>
    Key,

    /// <summary>A confirmed or successful value, rendered in bright green.</summary>
    Ok,

    /// <summary>A regular configuration value, rendered in muted green.</summary>
    Value,
}
